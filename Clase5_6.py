#items()  - Asociar la clave a una variable
#          Asociar el valor a otra variable
#para iterar diccionarios utilizando un for

diccionario = {
                'gato' : 'cat',
                'perro': 'dog',   
                'caballo': 'horse'}

for x1, y1 in diccionario.items():
    print(x1 , '->' , y1)
    


